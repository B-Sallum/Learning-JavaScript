/* Async / Await

Async> Essa palavra pode ser usada ao criarmos uma função convencional. Quando adicionamos esse identificador na criação desta função, nós definimos que ela será uma FUNÇÃO ASSÍNCRONA, ou seja, nos retornará UMA PROMISE.

Await: Essa palavra será usada com o objetivo DE ESPERAR a resolução de uma função assíncrona. Se houver uma série de série de funções, assíncronas, o await definirá que o código só terá sequência quando a função anterior for resolvida. O await só poderá ser usado como ASYNC FUNCIONTS (funções assícronas)

*/